import { Injectable } from '@angular/core';
import { ServiceProviderContact } from "./service-provider-contact";
import { TokenService } from '../../services/token.service';
import { Http, Headers, Response, URLSearchParams, RequestOptions } from "@angular/http";
import { Observable } from "rxjs";
import { DataService } from '../../services/data.service';
import { ServiceUrlConstants, ServerUrl } from '../../shared/service-url-constants';
import { Utility } from '../../shared/utility';
import { AuthenticationGuard } from '../../guards/authentication.guard';
import { ServiceProviderContactConstants } from './service-provider-contact.constants';
import { CRC } from './crc';
import { ServiceProvider } from '../../features/service-provider-contact/service-provider';


@Injectable()
export class ServiceProviderContactService {

    private serviceProviderContactList: ServiceProviderContact[] = [];
    private baseUrl: string = ServerUrl(ServiceUrlConstants.CMS) + ServiceUrlConstants.SERVICEPROVIDERCONTACT;

    constructor(private http: Http, private tokenService: TokenService, private dataService: DataService) {

    }

    getServiceProviderContactList(): Observable<ServiceProviderContact[]> {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.get(this.baseUrl, { headers: headers, body: "" }).map((response: Response) => response.json());
    }

    getServiceProviderContact(id: number): Observable<ServiceProviderContact> {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.get(this.baseUrl + "/" + id, { headers: headers, body: "" }).map((response: Response) => response.json());
    }

    updateServiceProviderContact(id: number, serviceProviderContact: ServiceProviderContact) {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.patch(this.baseUrl + "/" + id, serviceProviderContact, { headers: headers });
    }

    sortFilterAndPaginate(filterObj, paginationObj, sortObj) {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        let searchParams: URLSearchParams = new URLSearchParams();
        if (sortObj != null && sortObj.field != null && sortObj.field != "") {
            searchParams.append("sort", sortObj.field + "," + sortObj.sort);
        }
        if (paginationObj != null) {
            searchParams.append("size", paginationObj.size);
            searchParams.append("page", paginationObj.number);
        }
        let options = new RequestOptions({
            headers: headers,
            search: searchParams
        });
        return this.http.post(this.baseUrl + "/search", filterObj, options).map((response: Response) => response.json());
    }

    addServiceProviderContact(serviceProviderContact: ServiceProviderContact) {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.post(this.baseUrl, serviceProviderContact, { headers: headers }).map((response: Response) => response.json());
    }

    deleteServiceProviderContact(serviceProviderContactId: number) {
        const headers = new Headers();
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.delete(this.baseUrl + "/" + serviceProviderContactId, { headers: headers, body: "" });
    }

    getCrcList(): Observable<CRC[]>{
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.get(this.baseUrl + "/crc",{ headers: headers, body: "" }).map((response: Response) => <CRC[]> response.json());
    }

    getCrcByServiceIdList(id: number): Observable<CRC[]> {
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.get(this.baseUrl + "/crc/" + id, { headers: headers, body: "" }).map((response: Response) => response.json());
    }

    getServiceProviderNameList(): Observable<any[]>{
        const headers = new Headers();
        headers.append('Accept', 'application/json');
        headers.append('Content-Type', 'application/json');
        headers.append("X-Authorization", this.tokenService.getToken());
        return this.http.get(this.baseUrl + "/serviceprovider",{ headers: headers, body: "" }).map((response: Response) =><any[]> response.json());
    }
}
